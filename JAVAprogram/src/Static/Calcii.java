package Static;

public class Calcii {
	static int k;
	public static int add(int a,int b)
	{
		int c=a+b;
		return c;
	}
	
	public static int mul(int a,int b)
	{
		return a*b;
	}
	
	public static int div(int b,int a)
	{
		k=10;
		return b/k;
	}
	public static double sub(double a,double b)
	{
		return a-b;
	}
	
	

}
