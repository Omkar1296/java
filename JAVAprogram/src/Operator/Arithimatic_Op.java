package Operator;

public class Arithimatic_Op {

	public static void main(String[] args) {
		int a=10;
		int b=5;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);// it is Quotient
		System.out.println(a%b);// modulus is reminder
		System.out.println((a+b)+" "+(a-b)+" "+(a*b)+" "+(a/b)+" "+(a%b)+" ");
		System.out.println(a+b+" total "+a+b);
		

	}

}
