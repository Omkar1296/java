package Operator;
//their are two type of shift opreator
//<< left shift op    ex.10<<2= 10 *2^2=10*4=40
//>>right shift Op  ex.10>>2=10/2^2=10/4=2 here 10>>3=10/2^3=10/8=1

public class Shift_Op {

	public static void main(String[] args) {
		int a=10<<2;
		int b=10>>3;
		System.out.println(a);
		System.out.println(b);

	}

}
